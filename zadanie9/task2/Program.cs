using System;
using System.Collections.Generic;
using System.Linq;

public class Книга
{
    public string Название { get; set; }
    public string Автор { get; set; }
    public int ГодИздания { get; set; }
    public string Категория { get; set; }

    public Книга(string название, string автор, int годИздания, string категория)
    {
        Название = название;
        Автор = автор;
        ГодИздания = годИздания;
        Категория = категория;
    }
}

public class ДомашняяБиблиотека
{
    private List<Книга> книги = new List<Книга>();

    public void ДобавитьКнигу(Книга книга)
    {
        книги.Add(книга);
    }

    public bool УдалитьКнигу(string название)
    {
        var книга = книги.FirstOrDefault(k => k.Название == название);
        return книга != null && книги.Remove(книга);
    }

    public Книга НайтиПоАвтору(string автор)
    {
        return книги.FirstOrDefault(k => k.Автор == автор);
    }

    public IEnumerable<Книга> НайтиПоГодуИздания(int год)
    {
        return книги.Where(k => k.ГодИздания == год);
    }

    public IEnumerable<Книга> НайтиПоКатегории(string категория)
    {
        return книги.Where(k => k.Категория == категория);
    }

    public Книга ПолучитьПоНомеру(int номер)
    {
        if (номер >= 0 && номер < книги.Count)
        {
            return книги[номер];
        }
        return null;
    }
}

class Program
{
    static void Main()
    {
        ДомашняяБиблиотека библиотека = new ДомашняяБиблиотека();
        библиотека.ДобавитьКнигу(new Книга("Война и мир", "Л. Толстой", 1869, "Роман"));
        библиотека.ДобавитьКнигу(new Книга("Преступление и наказание", "Ф. Достоевский", 1866, "Роман"));

        var книгаПоАвтору = библиотека.НайтиПоАвтору("Л. Толстой");
        Console.WriteLine($"Найдена книга: {книгаПоАвтору.Название}");

        var книгиПоГоду = библиотека.НайтиПоГодуИздания(1866);
        foreach (var книга in книгиПоГоду)
        {
            Console.WriteLine($"Книга {книга.Название} была издана в {книга.ГодИздания} году.");
        }

        var удалитьУспешно = библиотека.УдалитьКнигу("Война и мир");
        Console.WriteLine($"Удаление книги 'Война и мир' выполнено: {удалитьУспешно}");

        var книгаПоНомеру = библиотека.ПолучитьПоНомеру(0);
        if (книгаПоНомеру != null)
        {
            Console.WriteLine($"Книга по номеру: {книгаПоНомеру.Название}");
        }
        Console.ReadLine();
    }
}
